							2. มีความสนใจทางด้านคอมพิวเตอร์ (ไม่จำเป็นต้องมีพื้นฐานมาก่อน)<br/>
							3. ได้รับอนุญาตจากผู้ปกครองให้เข้าร่วมโครงการเป็นลายลักษณ์อักษร<br/>
							4. สนใจที่จะเข้าร่วมโครงการ และปฏิบัติตามกิจกรรมที่จัดไว้ด้วยความตั้งใจอย่างเต็มที่<br/>
							5. สามารถพักค้างแรมตามระยะเวลาและสถานที่ที่กำหนดไว้ได้</p>

