							<p><b>- อาหาร</b> มีอาหารรับประทาน 3 มื้อและของว่าง ฟรีตลอดโครงการ<br/>
							<b>- พยาบาล</b> มีเครื่องมือและหน่วยพยาบาล พร้อมดูแลผู้เข้าร่วมโครงการตลอด 24 ชั่วโมง<br/>
							<b>- การเดินทาง</b> มีรถรับ-ส่ง ผู้เข้าร่วมโครงการตามสถานที่ที่ระบุไว้ในใบสมัคร<br/>

