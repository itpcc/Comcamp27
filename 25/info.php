							7. ใบรับรองความประพฤติจากอาจารย์ที่ปรึกษา (แนบมาพร้อมใบสมัคร)<br/>
							8. ภาพถ่ายชุดนักเรียน ถ่ายไว้ไม่เกิน 6 เดือน ขนาด 3x4 ซม. ติดในใบสมัคร 1 ภาพ และแนบมาพร้อมกับใบสมัคร 2 ภาพและภาพถ่ายอิสระที่น้องคิดว่าเห็นหน้าน้องชัดที่สุด (ภาพที่มีน้องคนเดียวเท่านั้น) ขนาด 4x6 นิ้ว จำนวน 1 ภาพ พร้อมเขียนชื่อ-สกุล และโรงเรียนหลังภาพถ่ายทุกใบ<br/><br/>

