							<h1><span class="glyphicon glyphicon-question-sign" aria-hidden="true"></span> ถาม-ตอบ</h1><hr>
							<p class="question">Q: พี่คะ หนูไม่เป็น/ไม่ชำนาญด้านทักษะคอมพิวเตอร์เลย จะสมัครได้มั้ยคะ?</p>
							<p>A: ได้ค่ะ เพราะโครงการคอมแคมป์เป็นโครงการที่มีวัตถุประสงค์เพื่อแนะนำการเรียนการสอนและการปฏิบัติเชิงคอมพิวเตอร์อยู่แล้ว ขอแค่น้องมีความสนใจด้านคอมพิวเตอร์และมีคุณสมบัติตามที่ระบุไว้ในใบสมัครก็สามารถสมัครเข้าร่วมโครงการได้ค่ะ</p>
							<hr>
							<p class="question">Q: ต้องพักค้างคืนที่ค่ายหรือเปล่าคะ?</p>
							<p>A: ใช่ค่ะ เพราะกิจกรรมบางช่วงระหว่าง 5 วัน 4 คืนของโครงการ จะเสร็จสิ้นในช่วงค่ำ พี่ๆจึงจัดให้น้องๆได้เข้าพักในหอพักนักศึกษาซึ่งสะดวกสบายและง่ายต่อการทำกิจกรรมของน้องๆ และที่สำคัญ พี่ๆจะดูแลน้องได้ตลอด 24 ชั่วโมงตลอดโครงการ น้องๆสบายใจได้ค่ะ :)</p>
							<hr>
							<p class="question">
								Q: หนูอยู่ต่างจังหวัด ต้องเดินทางมาเอง มีคำแนะนำมั้ยคะ?
							</p>
							<p>A: หากน้องเดินทางมาจากต่างจังหวัด สามารถระบุจุดนัดพบตามที่ระบุในใบสมัครไว้ ในวันที่น้องเดินทางมาเข้าร่วมโครงการ จะมีพี่ๆของโครงการไปดูแลและอำนวยความสะดวกในการเดินทางเข้าร่วมกิจกรรมที่จุดนั้นๆให้ค่ะ
								(พี่ๆที่ดูแลจะโทรศัพท์ไปยืนยันการเดินทางและนัดหมายกับน้องๆ 1 วันก่อนน้องเดินทางเป็นอย่างน้อย) ส่วนน้องๆที่อาศัยอยู่ในกรุงเทพฯ หากต้องการเดินทางมายังมหาวิทยาลัยด้วยตนเอง พี่มีเอกสารแนะนำการเดินทางมายังมหาวิทยาลัย โดยสามารถ <a href="http://27.comcamp.in.th/how-to/" target="_blank"><span class="glyphicon glyphicon-info-sign" aria-hidden="true"></span> <b>ดูข้อมูลการเดินทางได้ที่นี่</b></a> ค่ะ</p>
